var addJustMetaViewPorts = function (viewheight, viewwidth) {
    setTimeout(function () {
        var viewport = $("meta[name=viewport]");
        viewport.attr("content", "height=" + viewheight + "px, width=" + viewwidth + "px, initial-scale=1.0");
    }, 300);
}



var viewheight;
var viewwidth;
$(window).on("orientationchange", function() {
    var tm = viewwidth;
    viewwidth = viewheight;
    viewheight = tm;
    addJustMetaViewPorts(viewheight, viewwidth);
});

$(document).ready(function () {
    viewheight = $(window).height();
    viewwidth = $(window).width();
    addJustMetaViewPorts(viewheight, viewwidth);

    var navBottom = $(window).height();

    var scrollTopTop = function () {
        var scrollBottom = $(document).scrollTop();

        if (scrollBottom > navBottom) {
            $('#hs-back').addClass('hs-display');
        } else {
            $('#hs-back').removeClass('hs-display');
        }
    };

    scrollTopTop();
    addJustMetaViewPorts();

    $(window).scroll(function () {
        scrollTopTop();
    });

    $('#hs-back').click(function () {
        var body = $("html, body");
        body.stop().animate({scrollTop:0}, 500, 'swing', function() { 
        });
    });
});